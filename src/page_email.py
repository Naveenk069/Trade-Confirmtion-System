"""
page_email.py — Email Notification (P3 Feature).

Send generated trade confirmations via email using Python smtplib.
Supports Gmail SMTP (SSL) and generic SMTP servers.
Email config stored in data/email_config.json (admin-only setup).
"""

import streamlit as st
import json
import smtplib
import ssl
import os
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from email.mime.base import MIMEBase
from email import encoders
from pathlib import Path
from src.auth import log_audit

CONFIG_FILE = Path(__file__).parent.parent / "data" / "email_config.json"
POPULATED_DIR = Path(__file__).parent.parent / "data" / "populated"


def _load_config() -> dict:
    if CONFIG_FILE.exists():
        try:
            return json.loads(CONFIG_FILE.read_text())
        except Exception:
            pass
    return {}


def _save_config(cfg: dict):
    CONFIG_FILE.parent.mkdir(parents=True, exist_ok=True)
    CONFIG_FILE.write_text(json.dumps(cfg, indent=2))


def _send_email(cfg: dict, to_email: str, subject: str,
                body: str, attachment_path: Path = None) -> tuple[bool, str]:
    try:
        msg = MIMEMultipart()
        msg["From"]    = cfg["sender_email"]
        msg["To"]      = to_email
        msg["Subject"] = subject
        msg.attach(MIMEText(body, "html"))

        if attachment_path and attachment_path.exists():
            with open(attachment_path, "rb") as f:
                part = MIMEBase("application", "octet-stream")
                part.set_payload(f.read())
            encoders.encode_base64(part)
            part.add_header(
                "Content-Disposition",
                f'attachment; filename="{attachment_path.name}"'
            )
            msg.attach(part)

        context = ssl.create_default_context()
        port = int(cfg.get("smtp_port", 465))

        if port == 465:
            with smtplib.SMTP_SSL(cfg["smtp_host"], port, context=context) as server:
                server.login(cfg["sender_email"], cfg["smtp_password"])
                server.sendmail(cfg["sender_email"], to_email, msg.as_string())
        else:
            with smtplib.SMTP(cfg["smtp_host"], port) as server:
                server.ehlo()
                server.starttls(context=context)
                server.login(cfg["sender_email"], cfg["smtp_password"])
                server.sendmail(cfg["sender_email"], to_email, msg.as_string())

        return True, "Email sent successfully."
    except Exception as e:
        return False, str(e)


