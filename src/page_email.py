def page_email():
    st.markdown(
        '<div class="page-header">'
        '<div class="page-title">Email <span>Notifications</span></div>'
        '<div class="page-subtitle">Send generated trade confirmations directly to counterparties via email</div>'
        '</div>',
        unsafe_allow_html=True
    )

    role     = st.session_state.get("role", "analyst")
    username = st.session_state.get("user", {}).get("username", "unknown")
    user     = st.session_state.get("user", {}) or {}
    is_admin = role == "admin"
    cfg      = _load_config()

    # ── SMTP Setup (admin only) ───────────────────────────────────────────
    with st.expander("⚙️ SMTP Configuration" + (" (Admin Only)" if not is_admin else ""),
                     expanded=not cfg):
        if not is_admin:
            st.markdown(
                '<div class="alert alert-warn">⚠ SMTP configuration requires Admin role.</div>',
                unsafe_allow_html=True
            )
        else:
            st.markdown(
                '<div style="font-size:12px;color:rgba(255,255,255,0.4);margin-bottom:12px;">'
                'Configure your SMTP server to enable email sending. '
                'For Gmail: use smtp.gmail.com, port 465, and an App Password.</div>',
                unsafe_allow_html=True
            )
            c1, c2 = st.columns(2)
            with c1:
                smtp_host  = st.text_input("SMTP Host",
                    value=cfg.get("smtp_host", "smtp.gmail.com"),
                    key="em_host")
                smtp_port  = st.selectbox("SMTP Port",
                    [465, 587], index=0 if cfg.get("smtp_port", 465) == 465 else 1,
                    key="em_port")
                sender_email = st.text_input("Sender Email",
                    value=cfg.get("sender_email", ""),
                    placeholder="yourapp@gmail.com",
                    key="em_email")
            with c2:
                smtp_password = st.text_input("App Password / SMTP Password",
                    value=cfg.get("smtp_password", ""),
                    type="password",
                    key="em_pass",
                    help="For Gmail: generate an App Password in Google Account Security settings")
                sender_name = st.text_input("Sender Display Name",
                    value=cfg.get("sender_name", "TradeConfirmation AI"),
                    key="em_name")

            col1, col2 = st.columns(2)
            with col1:
                if st.button("💾 Save SMTP Config", key="save_smtp",
                             use_container_width=True):
                    new_cfg = {
                        "smtp_host":    smtp_host,
                        "smtp_port":    smtp_port,
                        "sender_email": sender_email,
                        "smtp_password":smtp_password,
                        "sender_name":  sender_name,
                    }
                    _save_config(new_cfg)
                    cfg = new_cfg
                    log_audit(username, "ADMIN", "SMTP config updated")
                    st.success("✅ SMTP configuration saved.")

            with col2:
                test_addr = st.text_input("Test email address", key="em_test_addr",
                                          placeholder="test@example.com",
                                          label_visibility="collapsed")
                if st.button("📧 Send Test Email", key="test_smtp",
                             use_container_width=True):
                    if not cfg.get("smtp_host") or not cfg.get("sender_email"):
                        st.error("Save SMTP config first.")
                    elif not test_addr:
                        st.error("Enter a test email address.")
                    else:
                        ok, msg = _send_email(
                            cfg, test_addr,
                            "Test — TradeConfirmation AI",
                            "<p>SMTP configuration is working correctly.</p>"
                        )
                        if ok:
                            st.success(f"✅ Test email sent to {test_addr}")
                        else:
                            st.error(f"❌ {msg}")

    if not cfg.get("smtp_host"):
        st.markdown(
            '<div class="alert alert-info">💡 SMTP not configured. '
            'Ask an admin to set up the email configuration.</div>',
            unsafe_allow_html=True
        )
        return

    st.markdown('<div class="grad-divider"></div>', unsafe_allow_html=True)

    # ── Send confirmation email ───────────────────────────────────────────
    st.markdown(
        '<div style="font-size:14px;font-weight:700;color:#fff;margin-bottom:16px;">'
        '📨 Send Trade Confirmation</div>',
        unsafe_allow_html=True
    )

    # List generated documents
